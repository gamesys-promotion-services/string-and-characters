package com.example.demo.characters;

public class CharacterEscapeSequences {

    public static void main(String[] args) {
        char tab = '\t';
        System.out.println("hello with tab" + tab + "world - tab");

        char backspace = '\b';
        System.out.println("hello with backspace" + backspace + "world - backspace");

        char newLine = '\n';
        System.out.println("hello with newline" + newLine + "world - newline");

        char carriageReturn = '\r';
        System.out.println("hello with carriageReturn" + carriageReturn + "world - carriageReturn");

        char formFeed = '\f';
        System.out.println("hello with formFeed" + formFeed + "world - formFeed");

        char singleQuote = '\'';
        System.out.println("hello with singleQuote" + singleQuote + "world - singleQuote");

        char doubleQuote = '\"';
        System.out.println("hello with doubleQuote" + doubleQuote + "world - doubleQuote");

        char backslash = '\\';
        System.out.println("hello with backslash" + backslash + "world - backslash");
    }

}
